    public void carga(){
        Thread hilo = new Thread(new Runnable(){
            public void run (){
                for ( int i=1;i<=100;i++){
                    try {
                        Barra.setValue(i);
                        Porcentaje.setText(i+" % ");
                        Thread.sleep(100);
                        if(i==100){
                            PrincipalPantalla p = new PrincipalPantalla();
                            p.setVisible(true);
                            setVisible(false);
                        }
                    } catch (InterruptedException ex) {
                        Logger.getLogger(CargaProceso.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }
            }
        });
        hilo.start();
    }
    
